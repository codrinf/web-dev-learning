// Some examples of actions you may perform with numbers (more to come):


// We declare 2 variables:
const a = 21; 
const b = 3;

// We calculate and log the result:
const sum = a + b;
console.log("sum of a and b = ", sum);

// We calculate difference and log the result:
const difference = a - b;
console.log("difference of a - b = ", difference);

// We calculate multiplication and log the result:
const multiplication = a * b;
console.log("multiplication of a and b = ", multiplication);

// We calculate multiplication and log the result:
const division = a / b;
console.log("division of a / b = ", division);

// We calculate the square of a and log the result:
const squareA = a * a;
console.log("square of a = ", squareA);

// We can also make multiple operations, with multiple variables:
const x = 10;
const y = 5;
const z = 2;

const result1 = x + y + z;
const result2 = z - x * y;

console.log('result1 = ', result1);
console.log('result2 = ', result2);

// Of course, you can use variables and/or values:
const v = x + 10;
console.log('v = ', v);

const t = 105 - 5;
console.log('t = ', t);

// A variable can change it's value: (a variable introduced by let can be re-initiated as opposite of const)
let myNumber = x + 1;
console.log('myNumber 1 = ', myNumber);

myNumber = x + 2;
console.log('myNumber 2 = ', myNumber);

myNumber = myNumber * 10;
console.log('myNumber 3 = ', myNumber);



